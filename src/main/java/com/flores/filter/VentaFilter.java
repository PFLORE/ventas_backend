package com.flores.filter;

import java.util.Date;

public class VentaFilter {

	private Integer id;
	private Date fecha;
	
	public VentaFilter() {}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Date getFecha() {
		return fecha;
	}

	public void setFecha(Date fecha) {
		this.fecha = fecha;
	}
}
